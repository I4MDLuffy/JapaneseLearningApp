data class KanaEntry(val kana: String, val romaji: String)

data class KanaGroup(
    val id: String,
    val label: String,
    val entries: List<KanaEntry>,
)

private fun group(id: String, label: String, vararg pairs: Pair<String, String>) =
    KanaGroup(id, label, pairs.map { KanaEntry(it.first, it.second) })

